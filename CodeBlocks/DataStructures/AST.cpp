#include "AST.h"
//------------------------------------------------------------------------------

AST::AST(){
 Children = 0;
 Next     = 0;
}
//------------------------------------------------------------------------------

AST::~AST(){
 if(Children) delete Children;
 if(Next    ) delete Next;
}
//------------------------------------------------------------------------------

AST_Constant::AST_Constant(){
 mpq_init(Value);
}
//------------------------------------------------------------------------------

AST_Constant::~AST_Constant(){
 mpq_clear(Value);
}
//------------------------------------------------------------------------------

